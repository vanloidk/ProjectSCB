package scb.com.vn.model;

/**
 *
 * @author lydty
 */
public class IBTDEDetail {

    String name;
    String adding;
    String value;

    /**
     *
     * @return
     */
    public String getName() {
        return name;
    }

    /**
     *
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     *
     * @return
     */
    public String getAdding() {
        return adding;
    }

    /**
     *
     * @param adding
     */
    public void setAdding(String adding) {
        this.adding = adding;
    }

    /**
     *
     * @return
     */
    public String getValue() {
        return value;
    }

    /**
     *
     * @param value
     */
    public void setValue(String value) {
        this.value = value;
    }
}
