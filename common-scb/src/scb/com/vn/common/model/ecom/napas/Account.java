package scb.com.vn.common.model.ecom.napas;

/**
 *
 * @author tamnt12
 */
public class Account {
    private String number;
    private String name;
    private String type;
    private String personalId;
    private String phoneNumber;
    private String email;
    private String ibUsername;
    private String taxCode;
    private String hardToken;
    private String customerCode;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPersonalId() {
        return personalId;
    }

    public void setPersonalId(String personalId) {
        this.personalId = personalId;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getIbUsername() {
        return ibUsername;
    }

    public void setIbUsername(String ibUsername) {
        this.ibUsername = ibUsername;
    }

    public String getTaxCode() {
        return taxCode;
    }

    public void setTaxCode(String taxCode) {
        this.taxCode = taxCode;
    }

    public String getHardToken() {
        return hardToken;
    }

    public void setHardToken(String hardToken) {
        this.hardToken = hardToken;
    }

    public String getCustomerCode() {
        return customerCode;
    }

    public void setCustomerCode(String customerCode) {
        this.customerCode = customerCode;
    }
    
    
}
