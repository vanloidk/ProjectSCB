package scb.com.vn.common.model.iibpayment;


public class BillPaymentDetailType {
	private String billDetailId;
	private String billCustomerCode;
	private String billDetailMonth;
	private String billDetailMoneyAmount;
	private String billDetailPaymentFee;
       
        
        
    
	
        
        
	public String getBillDetailId() {
		return billDetailId;
	}
	public void setBillDetailId(String billDetailId) {
		this.billDetailId = billDetailId;
	}
	public String getBillCustomerCode() {
		return billCustomerCode;
	}
	public void setBillCustomerCode(String billCustomerCode) {
		this.billCustomerCode = billCustomerCode;
	}
	public String getBillDetailMonth() {
		return billDetailMonth;
	}
	public void setBillDetailMonth(String billDetailMonth) {
		this.billDetailMonth = billDetailMonth;
	}
	public String getBillDetailMoneyAmount() {
		return billDetailMoneyAmount;
	}
	public void setBillDetailMoneyAmount(String billDetailMoneyAmount) {
		this.billDetailMoneyAmount = billDetailMoneyAmount;
	}
	public String getBillDetailPaymentFee() {
		return billDetailPaymentFee;
	}
	public void setBillDetailPaymentFee(String billDetailPaymentFee) {
		this.billDetailPaymentFee = billDetailPaymentFee;
	}
}
