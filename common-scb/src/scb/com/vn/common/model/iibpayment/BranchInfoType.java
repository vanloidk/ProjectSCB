package scb.com.vn.common.model.iibpayment;


public class BranchInfoType {
	private String branchCode;
	private String branchName;
	private String branchNameVN;
	private String branchAddress;
	public String getBranchCode() {
		return branchCode;
	}
	public void setBranchCode(String branchCode) {
		this.branchCode = branchCode;
	}
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	public String getBranchNameVN() {
		return branchNameVN;
	}
	public void setBranchNameVN(String branchNameVN) {
		this.branchNameVN = branchNameVN;
	}
	public String getBranchAddress() {
		return branchAddress;
	}
	public void setBranchAddress(String branchAddress) {
		this.branchAddress = branchAddress;
	}
}
